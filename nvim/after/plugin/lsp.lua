		-- },
		{
			{ name = 'cmdline' }
		})
})

local capabilities = require('cmp_nvim_lsp').default_capabilities()

local function default_server_init(server_name)
	require('lspconfig')[server_name].setup
	{
		on_attach = on_attach,
		capabilities = capabilities
	}
end

require("mason-lspconfig").setup_handlers
{
	default_server_init, -- default handler
	-- ["specific-hanler-example"] = function () require("example").setup() end
}

